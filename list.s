/*
list of all functions from each file.
*/
	--RNG--
xorShift
	-Returns a random number
	-No parameters

	--ITEMS--
spawn
	-Creates a spawned item
	-One parameter: The type of item that should be spawned
	-Void

spawnMove
	-Moves all spawned items
	-No parameters
	-Void

obliterate
	-Deletes an item
	-One parameter: The YPosition of the item that should be deleted
	-Void

	--PLAYER--
playerMove
	-Moves the player left or right
	-One parameter: Left or Right
	-Void

	--MAP--
moveMap
	-Moves the relevant map items
	-No parameters
	-Void

countTiles
	-Returns number of tiles counted
	-No parameters
